n1 = int(input("Valor 1: "))
n2 = int(input("Valor 2: "))

print("A soma: {}".format(n1 + n2))
print("A Subtracao: {}".format(n1 - n2))
print("A Multiplicacao: {}".format(n1 * n2))
print("A Divisao: {:.2f}".format(n1 / n2))
